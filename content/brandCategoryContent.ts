/**
 * Builds unique-enough content for a combo that has no hand-written entry,
 * using brand facts, the Hebrew category name, the product count and a few
 * real model names so no two pages read alike.
 */
export function buildBrandCategoryContent(params: {
  brand: string;
  brandSlug: string;
  category: string;
  productCount: number;
  sampleNames: string[];
}): BrandCategoryContent {
  const descriptor = BRAND_DESCRIPTORS[params.brandSlug];
  const samples = params.sampleNames.slice(0, 3).join(", ");

  return {
    intro: [
      `מחפשים ${params.category} של ${params.brand} בצפון? בחדד יובל אלקטריק מרוכזים ${params.productCount} דגמי ${params.category} מבית ${params.brand}${descriptor ? `, ${descriptor}` : ""} — כל אחד עם מפרט מלא, בדיקת זמינות באתר והזמנה ישירה בוואטסאפ או בטלפון.`,
      `הצוות שלנו ישמח לעזור לכם לבחור את הדגם המתאים${samples ? ` (בין הדגמים הזמינים: ${samples})` : ""}, ולתאם משלוח והתקנה עד בית הלקוח בכל אזור הצפון — מנהריה ועד הגליל, העמקים והגולן.`,
    ],
    faq: [
      {
        question: `האם יש משלוח והתקנה של ${params.category} ${params.brand} עד הבית?`,
        answer:
          "כן — משלוח, הכנסה והתקנה בבית הלקוח מתואמים כחלק מההזמנה, בכל אזור הצפון. המועד והעלות נסגרים מראש בהצעת המחיר.",
      },
    ],
  };
}

/** Resolve curated content for a combo, or null if none exists. */
export function getCuratedBrandCategoryContent(
  brandSlug: string,
  categorySlug: string
): BrandCategoryContent | null {
  return CURATED[`${brandSlug}:${categorySlug}`] ?? null;
}
